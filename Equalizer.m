% --- Executes on button press in pushbutton4.
function pushbutton4_Callback(hObject, eventdata, handles) %数字滤波
global Fs;
global data;
global data2;
v1=get(handles.slider1,'Value');
v2=get(handles.slider2,'Value');
v3=get(handles.slider3,'Value');
v4=get(handles.slider4,'Value');
v5=get(handles.slider5,'Value');
v6=get(handles.slider6,'Value');
v7=get(handles.slider7,'Value');
v8=get(handles.slider8,'Value');
v10=get(handles.slider10,'Value');
%f = [0 0.0028 0.0057 0.0113 0.0227 0.0454 0.0907 0.1814 0.3628 0.7256 1];
%f = [0 0.003 0.006 0.011 0.023 0.045 0.091 0.181 0.363 0.726 1];
f = [0 0.00136054 0.0028 0.0057 0.0113 0.0227 0.0454 0.0907 0.1814 0.3628 1];
m = [0 v1 v2 v3 v4 v5 v6 v7 v8 v10 0];
b = fir2(100,f,m);
data2 = filter(b,1,data);
N = length(data);
dt = 1.0/Fs;
T = N*dt;
t = linspace(0, T, N);
plot(handles.axes2,t,data2);
Amax=max(data2);
ylim(handles.axes2,[-Amax,Amax]);
[h, w]=freqz(b,1,128);
hold off
plot(handles.axes3,f,m,'o','MarkerSize',4)
hold on
plot(handles.axes3,w/pi,abs(h))
grid;



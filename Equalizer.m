% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles) %选择音频文件
global abc;
global len;
global Fs;
global data;

[FileName, PathName, filterindex]=uigetfile('*.wav','File Selector');
set(handles.edit1,'String',FileName);
abc=fullfile(PathName,FileName);

[data, Fs] = audioread(abc);
len = size(data);
dt = 1.0/Fs;
N = length(data);
T = N*dt;
t = linspace(0,T,N);
plot(handles.axes1,t,data);
Amax=max(data);
ylim(handles.axes1,[-Amax,Amax]);

